
public class VariablesExample3 {

	public static void main(String[] args) {
		
		// Declaring variables
		int num;
		int num1;
		int num2;
		int total;
		
		// Assignment statements
		num = 10;
		num1 = 30;
		num2 = 15;
		
		// Assignment statement for calculations
		
		total = num * (num1 + num2);
		
		System.out.print("The total is: $");
		System.out.println(total);
		
		

	}

}
