
public class MathLibraryExamples {

	public static void main(String[] args) {
		
		// Finding the square root
		double num = 49;
		double x = Math.sqrt(num);
		System.out.println(x);
		
		
		// Finding an exponent (power of a number)
		
		double base = 6.0;
		double exponent = 3.0;
		double y = Math.pow(base, exponent);
		System.out.println(y);
		
		// Finding an absolute number of a value.
		
		int num3 = -100;
		System.out.println( Math.abs(num3) );
		
		

	}

}
