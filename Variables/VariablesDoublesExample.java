
public class VariablesDoublesExample {

	public static void main(String[] args) {
		
		double total;
		double applesPrice;
		int numOfApples;
		
		applesPrice = 0.50;
		numOfApples = 15;
		
		total = applesPrice * numOfApples;
		
		
		System.out.print("The total price for buying ");
		System.out.print(numOfApples);
		System.out.print(" apples");
		System.out.print(" at $");
		System.out.print(applesPrice);
		System.out.print(" each is $");
		System.out.println(total);
				

	}

}
